# operadores atribuição

# = atribui
var1 = 2
puts var1

# += (adição)
var1 += 4 # var1 = var1 + 4
puts var1

# -= (subtração)
var1 -= 3 # var1 = var1 - 3
puts var1

# *= (multiplicão)
var1 *= 3 # var1 = var1 * 3
puts var1

# /= (divisão)
var1 /= 3 # var1 = var1 / 3
puts var1

# %= (módulo)
var1 %= 2 # var1 = var1 % 2
puts var1 

# **= (esponenciação)
var1 = 2
var1 **= 2 # var1 = var1 ** 2
puts var1